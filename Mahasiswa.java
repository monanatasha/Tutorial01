public class Mahasiswa {
	public static float ipk;
	public static String nama;
	public static String npm;
	
	public Mahasiswa() {
		this.ipk = ipk;
		this.nama = nama;
		this.npm = npm;
	}

	public static float getIpk() {
		return ipk;
	}

	public static void setIpk(float ipk) {
		Mahasiswa.ipk = ipk;
	}

	public static String getNama() {
		return nama;
	}

	public static void setNama(String nama) {
		Mahasiswa.nama = nama;
	}

	public static String getNpm() {
		return npm;
	}

	public static void setNpm(String npm) {
		Mahasiswa.npm = npm;
	}
}
